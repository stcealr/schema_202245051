CREATE TABLE employees (
    emp_id VARCHAR(255),
    name VARCHAR(255),
    name_eng VARCHAR(255),
    title VARCHAR(255),
    gender VARCHAR(255),
    birth_date VARCHAR(255),
    hire_date VARCHAR(255),
    address VARCHAR(255),
    city VARCHAR(255),
    region VARCHAR(255),
    home_phone VARCHAR(255),
    manager_id VARCHAR(255),
    dept_id VARCHAR(255)
);
